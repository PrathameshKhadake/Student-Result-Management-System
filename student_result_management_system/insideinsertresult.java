    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String ew = jTextField4.getText();
        String vlsi = jTextField5.getText();
        String vhdl = jTextField6.getText();
        String os = jTextField7.getText();
        
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Pratham@12");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,maths,ew,vlsi,vhdl,os) values('"+rollno+"','"+physics+"','"+maths+"','"+ew+"','"+vlsi+"','"+vhdl+"','"+os+"')");
                JOptionPane.showInternalMessageDialog(null,"Result is saved successfully");
                setVisible(false);
                new insideinsertresult().setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null,"Student is Not Registered!");
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

